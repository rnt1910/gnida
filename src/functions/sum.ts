/**
 * Напиши функцию, которая складывает два числа
 * Нужно написать логику функции так, чтобы `console.log(sum(5, 5))` выдал значение 10
 * Типизируй функцию:
 *  - задай тип аргументам (num1 и num2)
 *  - задай возвращаемый тип функции (подсказка: `number`)
 */
function sum(num1: number, num2: number) {
  return num1 + num2;
}

console.log(sum(5, 5)); // 10

